'''
练习2：**数据过滤**

过滤出列表中给定的字典数据
d1 = {'A': 65, 'B': 66, 'C': 67, 'D': 68, 'E': 69, 'F': 70}
L1 = ['A', 'C', 'F']

输出： {'A': 65, 'C': 67,  'F': 70}

分析：
    1 创建空字典 dict_data
    2 循环遍历：L1  --> x
        得到：'A', 'C', 'F'
        存储：dict_data[x] = d1[x]
'''

d1 = {'A': 65, 'B': 66, 'C': 67, 'D': 68, 'E': 69, 'F': 70}
L1 = ['A', 'C', 'F']

dict_data = {}

for x in L1:
    dict_data[x] = d1[x]

print(dict_data)

