"""
    拷贝：一分二的过程,防止数据被意外破坏
    浅拷贝：备份浅层数据,共享深层数据
        优点：占用内存较少
        缺点：深层数据被修改时,互相影响
        适用性：优先
    深拷贝：备份所有层数据
        优点：拷贝前后的数据绝对互不影响
        缺点：占用内存较多
        适用性：深层数据可能被修改
"""
list_name = [["杨建宏","马鹏"], ["潘兴兴","陈亦菲"]]
data01 = list_name[:] #  浅拷贝
data01[0][0] = "宏宏" # 修改深层数据
print(list_name)

# 准备深拷贝工具
import copy

list_name = [["杨建宏","马鹏"], ["潘兴兴","陈亦菲"]]
# 深拷贝
data01 = copy.deepcopy(list_name)
data01[0][0] = "宏宏"
print(list_name)
