"""
    字符串 --> 列表
        列表 = 字符串.split("分隔符")
"""
# 一个字符串 --分割--> 多个元素
list_result = "唐僧,孙悟空,八戒".split(",")
print(list_result)  # ['唐僧', '孙悟空', '八戒']

