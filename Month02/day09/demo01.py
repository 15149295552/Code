"""
    函数参数
        星号元组形参
        双星号字典形参
            调用者不必自行构建容器,而是由python自动构建容器
"""


# 星号元组形参:将多个位置实参,合并为一个元组
def func01(*args):
    print(args)


# 双星号字典形参:将多个关键字实参,合并为一个字典
def func02(**kwargs):
    print(kwargs)


func01()  # 空元组
func01(1)  # (1,)
func01(1, 2)  # (1,2)
# func01(p = 1) # 不支持关键字实参
func02(p1=1, p2=2)  # {'p1': 1, 'p2': 2}
